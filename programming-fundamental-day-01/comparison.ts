// Comparison Operator

let x: any = 5
let y: any = "5"

// 1. Equal (mengecek nilai saja)
console.log('x == y : ', x == y)

// 2. Strict Equal (mengecek nilai dan tipe data)
console.log('x === y : ', x === y)

// 3. Not Equal (tidak sama dengan nilainya)
console.log('x != y : ', x != y)

// 4. Strict Not Equal (tidak sama dengan nilainya dan tipe datanya)
console.log('x !== y : ', x !== y)

// 5. Greater than
console.log("x > 10 : ", x > 10)

// 6. Less than
console.log("x < 6 : ", x < 6)

// 7. Greater than or equal
console.log("x >= 5 : ", x >= 5 )

// 8. Less than or equal
console.log("x <= 5 : ", x <= 5)